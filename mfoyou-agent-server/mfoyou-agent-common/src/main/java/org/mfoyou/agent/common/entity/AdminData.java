package org.mfoyou.agent.common.entity;

public class AdminData {
	private Integer newUser;
	private Integer newStore;
	private Integer newAgent;
	public Integer getNewUser() {
		return newUser;
	}
	public void setNewUser(Integer newUser) {
		this.newUser = newUser;
	}
	public Integer getNewStore() {
		return newStore;
	}
	public void setNewStore(Integer newStore) {
		this.newStore = newStore;
	}
	public Integer getNewAgent() {
		return newAgent;
	}
	public void setNewAgent(Integer newAgent) {
		this.newAgent = newAgent;
	}
	
}
